export abstract class Generator<OUT extends string | number = string> {
  abstract generate(): OUT | Promise<OUT>;
}

export class UUIDGenerator extends Generator<string> {
  generate(): Promise<string> | string {
    return "xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx".replace(
      /[xy]/g,
      function (c) {
        const r = (Math.random() * 16) | 0,
          v = c == "x" ? r : (r & 0x3) | 0x8;
        return v.toString(16);
      }
    );
  }
}

